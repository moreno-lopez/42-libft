#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		src_len;
	char	*dest;

	src_len = ft_strlen(s);
	dest = (char *) malloc(sizeof(char) * src_len + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
